int		ft_iterative_factorial(int nb)
{
	int fact;

	if (nb >= 0 && nb < 13)
	{
		fact = 1;
		while (nb >= 1)
		{
			fact *= nb;
			nb--;
		}
		return (fact);
	}
	return (0);
}
